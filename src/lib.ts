/**
 * Add two numbers and return the result
 * @param x
 * @param y
 * @returns
 */
function add(x: number, y: number): number {
  return x + y;
}

/**
 * Multiply two numbers and return the result
 * @param x
 * @param y
 * @returns
 */
function mult(x: number, y: number): number {
  return x * y;
}

export { add, mult };
